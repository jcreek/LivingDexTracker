-- Complete regional pokédx setup with full PokéAPI data
-- This creates the normalized structure and populates with all official regional pokédx entries

-- Create the normalized regional pokédx entries table
CREATE TABLE regional_pokedex_entries (
  id BIGSERIAL PRIMARY KEY,
  pokedex_entry_id BIGINT NOT NULL REFERENCES pokedex_entries(id) ON DELETE CASCADE,
  regional_pokedex_name TEXT NOT NULL REFERENCES regional_pokedex_info(name) ON DELETE CASCADE,
  regional_number INTEGER NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  
  -- Ensure one entry per pokemon per regional dex
  UNIQUE(pokedex_entry_id, regional_pokedex_name),
  -- Ensure unique regional numbers within each dex
  UNIQUE(regional_pokedex_name, regional_number)
);

-- Create indexes for performance
CREATE INDEX idx_regional_pokedex_entries_pokedex_entry_id ON regional_pokedex_entries(pokedex_entry_id);
CREATE INDEX idx_regional_pokedex_entries_regional_pokedex_name ON regional_pokedex_entries(regional_pokedex_name);
CREATE INDEX idx_regional_pokedex_entries_regional_number ON regional_pokedex_entries(regional_pokedex_name, regional_number);

-- Enable RLS
ALTER TABLE regional_pokedex_entries ENABLE ROW LEVEL SECURITY;

-- RLS policy for public read access
CREATE POLICY "Anyone can view regional pokedex entries" ON regional_pokedex_entries
  FOR SELECT USING (true);

